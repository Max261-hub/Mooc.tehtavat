#Kirjoita ohjelma, joka kysyy käyttäjältä merkkijonon ja määrän ja
#  tulostaa sitten annettua merkkijonoa annetun määrän.
#  Tulostuksen tulee tapahtua yhdelle riville yhteen pötköön.

# Kysytään käyttäjältä merkkijono ja määrä
merkijono = input("Anna merkkijono: ")
maara = int(input("Anna määrä: "))

# Tulostetaan merkkijono toistettuna annetun määrän verran
print(merkijono * maara)
